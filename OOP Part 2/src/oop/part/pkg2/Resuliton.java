package oop.part.pkg2;

/**
 *
 * @author pc
 */
public class Resuliton {
    private double en;
    private double boy;

    public Resuliton(double en, double boy) {
        this.en = en;
        this.boy = boy;
    }

    /**
     * @return the en
     */
    public double getEn() {
        return en;
    }

    /**
     * @return the boy
     */
    public double getBoy() {
        return boy;
    }

    /**
     * @param en the en to set
     */
    public void setEn(double en) {
        this.en = en;
    }

    /**
     * @param boy the boy to set
     */
    public void setBoy(double boy) {
        this.boy = boy;
    }
    
}
